# List, Tuple, and Set
def list_tuple_set():
    # List
    my_list = [1, 22, 3, 4, 5]
    print("List:", my_list)

    # Tuple
    my_tuple = (1, 2, 3, 4, 5)
    print("Tuple:", my_tuple)

    # Set
    my_set = {1, 2, 3, 4, 5}
    print("Set:", my_set)

# Call the function
list_tuple_set()



# list are mutable, tuple are immutable, set mutable
# list are not unique, tuple are not unique, set are unique
# list are defined using square brackets, tuple are defined using circle brackets, set are defined using curly braces